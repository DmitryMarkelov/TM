package com.tagmarshal.golf.rest.model;

public class Fields {
    private String id;
    private String label;
    private String key;
    private boolean required;
    private String keyboard;

    public String getId() {
        return id;
    }

    public String getLabel() {
        return label;
    }

    public String getKey() {
        return key;
    }

    public boolean isRequired() {
        return required;
    }

    public String getKeyboard() {
        return keyboard;
    }
}
